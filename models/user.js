///////////////////////////////////////////////////////////
// User resourse (schema and model)
///////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////
// Import our dependencies
///////////////////////////////////////////////////////////
const mongoose = require ('./connection')

const { Schema, model } = mongoose

///////////////////////////////////////////////////////////
// define our schema
///////////////////////////////////////////////////////////
const userSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
})

const User = model("User", userSchema)

///////////////////////////////////////////////////////////
// Export our model
///////////////////////////////////////////////////////////
module.exports = User